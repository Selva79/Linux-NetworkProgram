#include<sys/signal.h>

/*-----------------------------------------------------------------------------------------------------------*/

                                            
int flag; 



void sigint_handler()                                                             /*------------------------------------ FUNCTION DEFINITION ------------------------------------*/
{
	printf("\n user defined SIGINT handler invoked to handle CTRL C. \n");
	flag=1;
	return;
}


int main()                                                       /* Start of main()                                                    */
{
	printf("\n Pressr Ctrl+c Within 5 Seconds To Generate Signal\n");
	signal(SIGINT,sigint_handler);
	sleep(5);
	if (flag==0)
 		printf("\n SIGINT not generated by the user !!!\n");
}


